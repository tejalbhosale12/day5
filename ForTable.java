package Day5;
import java.util.*;
public class ForTable {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter a number");
		int num=sc.nextInt();
		int i=1;
		int mult;
		for(i=1;i<=10;i++) {
			mult=i*num;
			System.out.println(mult);
			
	}
	}
}
